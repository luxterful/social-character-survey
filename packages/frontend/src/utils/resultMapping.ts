export function mapResult(questionNumber: number, value: 'J' | 'V' | 'N') {
  const q = scoreMapping[questionNumber]
  const keys = Object.keys(q)
  const res: any = {}
  for (const key of keys) {
    const score = q[key][value]
    if (score) {
      res[key] = score
    }
  }
  return res
}
